package com.pravin.lede.gl.myapplication.models;


public class Info {

    String name;
    String dob;

    public Info(String name, String dob) {
        this.name = name;
        this.dob = dob;
    }

    public String getName() {
        return name;
    }

    public String getDob() {
        return dob;
    }
}
