package com.pravin.lede.gl.myapplication.models;

public class ActivityItems {

    int image;
    String item;

    public ActivityItems(int image, String item) {
        this.image = image;
        this.item = item;
    }

    public int getImage() {
        return image;
    }

    public String getItem() {
        return item;
    }
}
